public class SumasTransacciones {

    double sumMonto, sumSaldo, sumMontoColones, sumSaldoColones;
    String moneda;

    public double getSumMonto() {
        return sumMonto;
    }

    public void setSumMonto(double sumMonto) {
        this.sumMonto = sumMonto;
    }

    public double getSumSaldo() {
        return sumSaldo;
    }

    public void setSumSaldo(double sumSaldo) {
        this.sumSaldo = sumSaldo;
    }

    public double getSumMontoColones() {
        return sumMontoColones;
    }

    public void setSumMontoColones(double sumMontoColones) {
        this.sumMontoColones = sumMontoColones;
    }

    public double getSumSaldoColones() {
        return sumSaldoColones;
    }

    public void setSumSaldoColones(double sumSaldoColones) {
        this.sumSaldoColones = sumSaldoColones;
    }

    public String getMoneda() {
        return moneda;
    }

    public void setMoneda(String moneda) {
        this.moneda = moneda;
    }

}
