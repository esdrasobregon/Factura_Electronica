public class ResumenProveedor {

    double sumMonto, sumSaldo, sumMonto_Colones, sumSaldo_Colones;
    String tipo_mora, proveedor;

    public double getSumMonto() {
        return sumMonto;
    }

    public void setSumMonto(double sumMonto) {
        this.sumMonto = sumMonto;
    }

    public double getSumSaldo() {
        return sumSaldo;
    }

    public void setSumSaldo(double sumSaldo) {
        this.sumSaldo = sumSaldo;
    }

    public double getSumMonto_Colones() {
        return sumMonto_Colones;
    }

    public void setSumMonto_Colones(double sumMonto_Colones) {
        this.sumMonto_Colones = sumMonto_Colones;
    }

    public double getSumSaldo_Colones() {
        return sumSaldo_Colones;
    }

    public void setSumSaldo_Colones(double sumSaldo_Colones) {
        this.sumSaldo_Colones = sumSaldo_Colones;
    }

    public String getTipo_mora() {
        return tipo_mora;
    }

    public void setTipo_mora(String tipo_mora) {
        this.tipo_mora = tipo_mora;
    }

    public String getProveedor() {
        return proveedor;
    }

    public void setProveedor(String proveedor) {
        this.proveedor = proveedor;
    }
    
    
}
